public class AsignaturaModelo {
    
    int idasignatura;
    String nombre_asignatura;
    String codigo_interno;
    int estado;
    int requisitoA;
    int requisitoB;
    int requisitoC;
    String codigo_europeo;

    public int getIdasignatura() {
        return idasignatura;
    }

    public void setIdasignatura(int idasignatura) {
        this.idasignatura = idasignatura;
    }

    public String getNombre_asignatura() {
        return nombre_asignatura;
    }

    public void setNombre_asignatura(String nombre_asignatura) {
        this.nombre_asignatura = nombre_asignatura;
    }

    public String getCodigo_interno() {
        return codigo_interno;
    }

    public void setCodigo_interno(String codigo_interno) {
        this.codigo_interno = codigo_interno;
    }

    public int getEstado() {
        return estado;
    }

    public void setEstado(int estado) {
        this.estado = estado;
    }

    public int getRequisitoA() {
        return requisitoA;
    }

    public void setRequisitoA(int requisitoA) {
        this.requisitoA = requisitoA;
    }

    public int getRequisitoB() {
        return requisitoB;
    }

    public void setRequisitoB(int requisitoB) {
        this.requisitoB = requisitoB;
    }

    public int getRequisitoC() {
        return requisitoC;
    }

    public void setRequisitoC(int requisitoC) {
        this.requisitoC = requisitoC;
    }

    public String getCodigo_europeo() {
        return codigo_europeo;
    }

    public void setCodigo_europeo(String codigo_europeo) {
        this.codigo_europeo = codigo_europeo;
    }
    
    
}
